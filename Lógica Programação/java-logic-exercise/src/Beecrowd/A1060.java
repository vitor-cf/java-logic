package Beecrowd;

import java.util.*;

public class A1060 {
    
    public static void main(String[] args) {

        Scanner sc = new Scanner(System.in);
        int contador = 1, qtd = 0;

        while (contador <= 6) {
           float valor = sc.nextFloat();
            if (valor > 0) {
                qtd++;
                
            }
            contador++;
        }
        System.out.println(qtd + " valores positivos");
        
    }
}
        


        
        
        

        

        
         
        
        


        
        
        

        
        


        
        
